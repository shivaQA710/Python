# Function for add of 2 number
def addtion(a,b):
    return a+b

# Function for substaraction of 2 number
def substraction(a,b):
    return a-b

# Function for multiplication of 2 number
def multiplication(a,b):
    return a*b

# Function for devision of 2 number
def devision(a,b):
    return a/b

a= int(input("Enter val of a : "))
